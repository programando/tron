<div>
	<div class="titleM1 colorBlue mb10 t18"><a name="recomendar"></a>Recomendar Producto</div>
    <div class="p20">

        Cuando te encuentres viendo un producto y consideres que dicho producto le puede especialmente interesar a un amigo tuyo, puede usar la opción
        <em>¡Recomienda este producto a un amigo!.</em>
        <br><br>
        Si perteneces al plan Empresario TRON, al compartir el producto, se relaciona automáticamente a quien lo recibe con tu código de usuario
        permitiendo que sus compras te  generen comisiones. Esto aplica bajo 2 condiciones: a) Si quien lo recibe al realizar la compra se registra
        en el plan Cliente, en el plan Empresario TRON o en el plan Comprador Ocasional. b) Si quien lo recibe y realiza la compra, ya es parte de tus Clientes o tu red de Empresarios TRON.

    </div>
</div>
<br><br>

<div>
	<div class="titleM1 colorBlue mb10 t18"><a name="registrar"></a>Registrar Amigos</div>
    <div class="p20">

